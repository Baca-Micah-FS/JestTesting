function add(num1, num2) {
  return num1 + num2;
}

function subtract(num1, num2) {
  return num1 - num2;
}

function multiply(num1, num2) {
  return num1 * num2;
}

function divide(num1, num2) {
  return num1 / num2;
}

function sqrt(num) {
  return Math.sqrt(num);
}

function max(num1, num2) {
  return Math.max(num1, num2);
}

module.exports = {
  add,
  subtract,
  multiply,
  divide,
  sqrt,
  max,
};
